export const ramSizes = [
  { size: "64GB", scoreMultiplier: 1 },
  { size: "32GB", scoreMultiplier: 0.95 },
  { size: "16GB", scoreMultiplier: 0.9 },
  { size: "8GB", scoreMultiplier: 0.8 },
  { size: "4GB", scoreMultiplier: 0.6 },
  { size: "2GB", scoreMultiplier: 0.4 }
];

// Get a component score by name
export const getComponentScore = (
  componentType: "cpu" | "gpu" | "ram",
  componentName: string,
  ramSize?: string
): number => {
  if (componentType === "cpu") {
    const cpu = cpuData.find(cpu => cpu.name.toLowerCase() === componentName.toLowerCase());
    return cpu ? cpu.score : 5; // Default score if not found
  } else if (componentType === "gpu") {
    const gpu = gpuData.find(gpu => gpu.name.toLowerCase() === componentName.toLowerCase());
    return gpu ? gpu.score : 5; // Default score if not found
  } else if (componentType === "ram" && ramSize) {
    const ramType = ramTypes.find(r => componentName.toUpperCase().includes(r.type));
    const size = ramSizes.find(s => s.size === ramSize);
    
    if (ramType && size) {
      return ramType.baseScore * size.scoreMultiplier;
    }
    return 5; // Default score if not found
  }
  
  return 5; // Default fallback score
};
